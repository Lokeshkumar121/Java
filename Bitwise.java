public class Bitwise {
    public static void main(String args[]){
        // bitwise operator
        int a = 10 , b = 3 ;
        System.out.println("bitwise and " + (a&b));
        System.out.println("bitwise or " + (a|b));
        System.out.println("bitwise exor " + (a^b));
    }    
}
